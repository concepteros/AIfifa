const fs = require("node:fs/promises");
const path = require("node:path");

const SOURCE_URL = "https://en.wikipedia.org/wiki/2026_FIFA_World_Cup_squads";
const OUTPUT_FILE = path.join(__dirname, "..", "squad-data.js");
const INPUT_FILE = process.argv[2];

const TEAM_CODES = new Map([
  ["czech republic", "CZE"], ["mexico", "MEX"], ["south africa", "RSA"], ["south korea", "KOR"],
  ["bosnia and herzegovina", "BIH"], ["canada", "CAN"], ["qatar", "QAT"], ["switzerland", "SUI"],
  ["brazil", "BRA"], ["haiti", "HAI"], ["morocco", "MAR"], ["scotland", "SCO"],
  ["australia", "AUS"], ["paraguay", "PAR"], ["turkey", "TUR"], ["united states", "USA"],
  ["curacao", "CUW"], ["curaçao", "CUW"], ["ecuador", "ECU"], ["germany", "GER"],
  ["ivory coast", "CIV"], ["japan", "JPN"], ["netherlands", "NED"], ["sweden", "SWE"],
  ["tunisia", "TUN"], ["belgium", "BEL"], ["egypt", "EGY"], ["iran", "IRN"],
  ["new zealand", "NZL"], ["cape verde", "CPV"], ["saudi arabia", "KSA"], ["spain", "ESP"],
  ["uruguay", "URU"], ["france", "FRA"], ["iraq", "IRQ"], ["norway", "NOR"],
  ["senegal", "SEN"], ["algeria", "ALG"], ["argentina", "ARG"], ["austria", "AUT"],
  ["jordan", "JOR"], ["colombia", "COL"], ["dr congo", "COD"], ["portugal", "POR"],
  ["uzbekistan", "UZB"], ["croatia", "CRO"], ["england", "ENG"], ["ghana", "GHA"], ["panama", "PAN"]
]);

function decodeHtml(value) {
  return value
    .replace(/&#(\d+);/g, (_, number) => String.fromCodePoint(Number(number)))
    .replace(/&#x([\da-f]+);/gi, (_, number) => String.fromCodePoint(parseInt(number, 16)))
    .replace(/&amp;/g, "&")
    .replace(/&quot;/g, '"')
    .replace(/&#39;|&apos;/g, "'")
    .replace(/&nbsp;/g, " ");
}

function clean(value) {
  return decodeHtml(value)
    .replace(/<[^>]+>/g, " ")
    .replace(/\[[^\]]+\]/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

function cellsFromRow(row) {
  return [...row.matchAll(/<td\b[^>]*>([\s\S]*?)<\/td>/gi)].map((match) => clean(match[1]));
}

function playerFromRow(row) {
  const cells = cellsFromRow(row);
  const name = clean(row.match(/<th\b[^>]*scope="row"[^>]*>([\s\S]*?)<\/th>/i)?.[1] || "");
  const position = clean(row.match(/<a\b[^>]*>(GK|DF|MF|FW)<\/a>/i)?.[1] || "");
  if (cells.length < 5 || !name || !position) return null;

  const ageMatch = cells.at(-4)?.match(/aged\s+(\d+)/i);
  return {
    age: ageMatch ? Number(ageMatch[1]) : null,
    caps: Number(cells.at(-3)) || 0,
    club: cells.at(-1),
    goals: Number(cells.at(-2)) || 0,
    name,
    position
  };
}

async function loadHtml() {
  if (INPUT_FILE) return fs.readFile(path.resolve(INPUT_FILE), "utf8");
  const response = await fetch(SOURCE_URL, {
    headers: { "User-Agent": "fifa2026-dashboard-squad-sync/1.0" }
  });
  if (!response.ok) throw new Error(`Wikipedia request failed: ${response.status}`);
  return response.text();
}

async function main() {
  const html = await loadHtml();
  const sections = html.split(/<div class="mw-heading mw-heading3"[^>]*>/i).slice(1);
  const squads = {};

  sections.forEach((section) => {
    const teamName = clean(section.match(/<h3\b[^>]*>([\s\S]*?)<\/h3>/i)?.[1] || "");
    const code = TEAM_CODES.get(teamName.toLowerCase());
    const table = section.match(/<table\b[^>]*class="[^"]*wikitable[^"]*"[^>]*>([\s\S]*?)<\/table>/i)?.[1];
    if (!code || !table) return;

    const players = [...table.matchAll(/<tr\b[^>]*>([\s\S]*?)<\/tr>/gi)]
      .map((match) => playerFromRow(match[1]))
      .filter(Boolean);
    if (players.length) squads[code] = players;
  });

  const playerCount = Object.values(squads).reduce((total, players) => total + players.length, 0);
  const output = [
    `// Generated by scripts/sync-wikipedia-squads.js from ${SOURCE_URL}`,
    `// Synced at ${new Date().toISOString()}`,
    `window.WORLD_CUP_SQUADS = ${JSON.stringify(squads, null, 2)};`,
    ""
  ].join("\n");

  await fs.writeFile(OUTPUT_FILE, output, "utf8");
  console.log(`Synced ${Object.keys(squads).length} squads and ${playerCount} players.`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
